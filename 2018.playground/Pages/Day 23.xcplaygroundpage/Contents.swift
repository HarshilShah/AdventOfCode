        for z in zRange {
            for y in yRange {
                for x in xRange {
                    let point = Nanobot(x: x, y: y, z: z, r: 0)
                    let count = scaledBots.count { $0.distance(to: point) <= $0.r }
                    
                    if (count > answerCount) || (count == answerCount && point.distance(to: .zero) < answer.distance(to: .zero) ) {
                        answer = point
                        answerCount = count
                    }
                }
            }
        }
        
        xRange = ((answer.x - 1) * 2) ... ((answer.x + 1) * 2)
        yRange = ((answer.y - 1) * 2) ... ((answer.y + 1) * 2)
        zRange = ((answer.z - 1) * 2) ... ((answer.z + 1) * 2)
        
        scale /= 2
    } while scale >= 1
    
    let result = answer.distance(to: .zero)
    return "\(result)"
}

